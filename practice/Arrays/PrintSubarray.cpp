#include <iostream> // O(N^3)
#include <map>
#include <list>
#include <queue>
#include <cstring>
#include <vector>
using namespace std;

int main()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = i; j < n; j++)
        {
            for (int k = i; k <= j; k++)
            {
                cout << a[k] << " ";
            }
            cout << endl;
        }
        cout << endl;
    }

    return 0;
}